#define FOR(x,y) for (int x = 0; x < y; ++x)
#define ffor(x,y,z) for (int x = y; x < z; ++x)
class Solution {///388ms 71.86% 28.9M 59.17%
public:
    int fourSumCount(vector<int>& A, vector<int>& B, vector<int>& C, vector<int>& D) {
        unordered_map<int,int> cnt;
        for (int x : A) for (int y : B) ++cnt[x + y];
        int rst = 0;
        for (int x : C) for (int y : D) if (cnt.count(-x - y)) rst += cnt[-x - y];
        return rst;
    }
};

class SolutionV1 {///552ms 29.15% 46.5M 13.39%
public:
    int fourSumCount(vector<int>& A, vector<int>& B, vector<int>& C, vector<int>& D) {
        unordered_map<int,int> cnt1, cnt2;
        for (int x : A) for (int y : B) ++cnt1[x + y];
        for (int x : C) for (int y : D) ++cnt2[x + y];
        int rst = 0;
        for (auto [x, y] : cnt1) if (cnt2.count(-x)) rst += y * cnt2[-x];
        return rst;
    }
};


