#define FOR(x,y) for (int x = 0; x < y; ++x)
#define ffor(x,y,z) for (int x = y; x < z; ++x)

/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {///12ms 26.38% 11M 13.86%
    using vi = vector<int>;
    vi data;

    int dfs (TreeNode* root, int x, int y, int lv = 0, int fa=0)
    {
        if (!root) return -1;
        if (root->val == x || root->val == y)
        {
            if (data.empty())
            {
                data.emplace_back(lv);
                data.emplace_back(fa);
            }
            else
            {
                if (lv == data[0] && fa != data[1]) return 1;
                else return 0;
            }
        }
        int rst = dfs(root->left, x, y, lv + 1, root->val);
        if (~rst) return rst;
        rst = dfs(root->right, x, y, lv + 1, root->val);
        if (~rst) return rst;
        return -1;
    }
public:
    bool isCousins(TreeNode* root, int x, int y) {
        if (x == y || !root || root->val == x || root->val == y) return 0;
        return dfs(root,x,y);
    }
};

