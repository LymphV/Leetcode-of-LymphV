#define FOR(x,y) for (int x = 0; x < y; ++x)
#define ffor(x,y,z) for (int x = y; x < z; ++x)
class Solution {///12ms 74.83% 10.7M 16.11%
public:
    vector<int> intersection(vector<int>& nums1, vector<int>& nums2) {
        unordered_set<int> cnt, rst;
        for(int x : nums1) cnt.insert(x);
        for(int x : nums2) if (cnt.count(x)) rst.insert(x);
        return vector<int>(begin(rst), end(rst));
    }
};



