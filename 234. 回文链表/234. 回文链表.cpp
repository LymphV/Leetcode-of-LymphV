/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
#define FOR(x,y) for (int x = 0; x < y; ++x)
#define ffor(x,y,z) for (int x = y; x < z; ++x)
class Solution {///44ms 12.81% 16.7M 5.02%
    ListNode * head;

    void solve (ListNode* now)
    {
        if (!now) return;
        solve(now->next);
        if (now == head) throw true;
        if (head->next == now) throw head->val == now->val;
        if (head->val != now->val) throw false;
        head = head->next;
    }

public:
    bool isPalindrome(ListNode* head) {
        if (!head) return true;
        this->head = head;
        try{solve(head);}
        catch (bool rst) {return rst;}
        return false;
    }
};


